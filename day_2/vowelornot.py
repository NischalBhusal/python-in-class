a = input("Enter a Letter: ")
if a == "A" or a == "a":
    print("A is a vowel")
elif a == "E" or a == "e":      
    print("E is a vowel")
elif a == "I" or a == "i":
    print("I is a vowel")
elif a == "O" or a == "o":    
    print("O is a vowel")      
elif a == "U" or a == "u":
    print("U is a vowel")
else:
    print("This is a consonant")